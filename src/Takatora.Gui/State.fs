/// Engine kind per project (project.toml [engine].type), for tinting the
/// Home project list. Best-effort: projects whose toml is missing/broken
/// are omitted. Recomputed whenever the project list changes. Self-contained
/// (reads p.Path directly) so it can run from `init`.
let private engineKindsFor (projects: ProjectRegistration list) : Map<ProjectId, EngineKind> =
    projects
    |> List.choose (fun (p: ProjectRegistration) ->
        let path = Path.Combine(p.Path, ".takatora", "project.toml")
        if not (File.Exists path) then None
        else try Some (p.Name, (TomlConfig.loadProject path).Engine.Kind) with _ -> None)
    |> Map.ofList

let init () : Model * Cmd<Msg> =
    let projects = ProjectRegistry.load ()
    let appSettings = AppSettings.load ()
    { OpenTabs       = [ Home ]
      ActiveTab      = Home
      Projects       = projects
      ProjectSubTabs = Map.empty
      ProjectHistory = Map.empty
      ProjectFlows   = Map.empty
      ProjectInfo    = Map.empty
      ProjectEngines = engineKindsFor projects
      ProjectSecrets = Map.empty
      ProjectToolbox = Map.empty
      ToolboxStates  = Map.empty
      ToolboxHistories = Map.empty
      ToolRunsInFlight = Set.empty
      ToolStopsRequested = Set.empty
      ToolRunErrors  = Map.empty
      ToolboxDirDraft = Map.empty
      ToolboxRecentOpen = Set.empty
      RunDetails     = Map.empty
      RunLogFilter   = Map.empty
      RunLogMatchIdx = Map.empty
      OpeningEditors = Set.empty
      OpeningIde     = Set.empty
      CopiedRunCmd   = Set.empty
      AppSettings    = appSettings
      IdeCommandDraft = appSettings.IdeCommand |> Option.defaultValue ""
      IdeCandidates  = []
      GodotSearchPathsDraft = String.concat "\n" appSettings.GodotSearchPaths
      GodotCandidates = []
      SelectedStep   = None
      StepSchemas    = Map.empty
      StepSchemasLoading = Set.empty
      AddStepDraft   = Map.empty
      ExpandedFlows  = Set.empty
      EditingFlows   = Set.empty
      SettingsFilter = ""
      Watches        = Map.empty
      WatchEnabled   = true
      LiveRuns       = Map.empty
      AddProject     = None
      CurrentProject = None
      RunDialog      = None
      ShowingAbout   = false
      ConfirmingQuit = false
      GrantPrompt    = None
      ProjectParams  = Map.empty
      ParamSecretDrafts = Map.empty },
    // A always-on poll timer for watch mode. It's a no-op when nothing is
    // watched; when a project is watched, WatchPoll samples its git HEAD.
    [ (fun dispatch ->
        async {
            do! Async.SwitchToThreadPool ()
            while true do
                do! Async.Sleep 5000
                Dispatcher.UIThread.Post(fun () -> dispatch WatchPoll)
        }
        |> Async.Start)
      // Let the tray's Pause/Resume item flip the global watch master. We
      // capture the real dispatch here (init's Cmds receive it) and marshal
      // to the UI thread, so the tray can drive Elmish without a back-channel.
      (fun dispatch ->
        TrayBridge.requestToggleGlobal <-
            fun () -> Dispatcher.UIThread.Post(fun () -> dispatch ToggleGlobalWatch)
        // Same pattern for the tray's Quit item, so quitting can pass
        // through RequestQuit's in-flight-tools check.
        TrayBridge.requestQuit <-
            fun () -> Dispatcher.UIThread.Post(fun () -> dispatch RequestQuit)) ]

let private moveOrAppend (tab: RootTab) (tabs: RootTab list) : RootTab list =
    if List.contains tab tabs then tabs else tabs @ [ tab ]

/// Look up the active sub-tab for a project, defaulting to Flows. Used
/// by views; not used by `update` (which writes the map directly).
let projectSubTab (pid: ProjectId) (model: Model) : ProjectSubTab =
    Map.tryFind pid model.ProjectSubTabs
    |> Option.defaultValue ProjectFlows

// ─── Per-project tab scoping ─────────────────────────────────────────
// The root strip used to show every open tab in one flat row, mixing
// projects together. These helpers let the view scope the strip to the
// "current project" (the one owning the active tab) so the user only
// ever sees one project's tabs at a time. None of this lives in the
// model — it's all derived from OpenTabs/ActiveTab, so there's no extra
// state to keep in sync.

/// The project a tab belongs to, if any. Home is global (None). A
/// LiveRun tab's project is read from its live state; an entry always
/// exists while the tab is open (LiveRunCompleted only drops the entry
/// when the tab is also closed), so this is reliable for open tabs.
let tabProject (model: Model) (tab: RootTab) : ProjectId option =
    match tab with
    | Home               -> None
    | Project pid        -> Some pid
    | RunDetail (pid, _) -> Some pid
    | LiveRun key ->
        Map.tryFind key model.LiveRuns
        |> Option.map (fun s -> s.ProjectId)

/// The project context the strip is scoped to. Sticky model state, not
/// derived from the active tab — so clicking Home keeps the project's
/// tabs visible rather than wiping the strip.
let currentProject (model: Model) : ProjectId option =
    model.CurrentProject

/// Distinct open project contexts, in first-seen order across OpenTabs.
/// Drives the project selector's choice list.
let openProjects (model: Model) : ProjectId list =
    model.OpenTabs
    |> List.choose (tabProject model)
    |> List.distinct

/// Tabs the strip should show: Home always (it's a global overlay),
/// plus the tabs belonging to the current project context. With no
/// context (CurrentProject = None) only Home shows.
let visibleTabs (model: Model) : RootTab list =
    let ctx = model.CurrentProject
    model.OpenTabs
    |> List.filter (fun tab ->
        match tab with
        | Home -> true
        | _    -> tabProject model tab = ctx)

/// Stable, human-friendly serial number for a run within its project,
/// derived from chronological order: the oldest run is #1, the newest is
/// #N. `RunHistory.load` returns newest-first, so the number is
/// `count - index`. Stays stable as new runs are appended (existing runs
/// keep their number). None when the run isn't in the cached history.
let runNumber (model: Model) (pid: ProjectId) (rid: RunId) : int option =
    match Map.tryFind pid model.ProjectHistory with
    | Some entries ->
        match List.tryFindIndex (fun (e: RunHistoryEntry) -> e.RunId = rid) entries with
        | Some i -> Some (entries.Length - i)
        | None   -> None
    | None -> None

/// Flow id recorded for a run, looked up from the cached history.
let runFlowId (model: Model) (pid: ProjectId) (rid: RunId) : string option =
    match Map.tryFind pid model.ProjectHistory with
    | Some entries ->
        entries
        |> List.tryFind (fun (e: RunHistoryEntry) -> e.RunId = rid)
        |> Option.map (fun e -> e.FlowId)
    | None -> None

/// The run whose serial number is `offset` away from `rid`'s (e.g. +1 =
/// the next-newer run #N+1, -1 = the next-older #N-1). Drives the
/// prev/next navigation on a RunDetail tab. None at the ends of history
/// or when `rid` isn't in the cached list. History is newest-first, so a
/// higher number sits at a lower index → target index = i - offset.
let runByNumberOffset (model: Model) (pid: ProjectId) (rid: RunId) (offset: int) : RunId option =
    match Map.tryFind pid model.ProjectHistory with
    | Some entries ->
        match List.tryFindIndex (fun (e: RunHistoryEntry) -> e.RunId = rid) entries with
        | Some i ->
            let targetIdx = i - offset
            if targetIdx >= 0 && targetIdx < List.length entries
            then Some (List.item targetIdx entries).RunId
            else None
        | None -> None
    | None -> None

let private projectRoot
        (pid: ProjectId)
        (projects: ProjectRegistration list)
        : string option =
    projects
    |> List.tryFind (fun (p: ProjectRegistration) -> p.Name = pid)
    |> Option.map (fun p -> p.Path)

/// Stop handles for in-flight tool runs, keyed like ToolRunsInFlight.
/// A mutable side-table rather than model state: a handle is a live
/// closure over the running Process, which has no business inside the
/// Elmish model. Written from the worker thread (runToolWith's
/// onStarted), read from the UI thread (StopTool) — hence Concurrent.
let private toolRunHandles =
    System.Collections.Concurrent.ConcurrentDictionary<ProjectId * string, ToolRunHandle>()

/// Kill every in-flight toolbox run (whole process trees). Hooked to the
/// app's shutdown (desktop.Exit in Program) so scripts the GUI started
/// never outlive a Quit. Stop is idempotent and swallows late/dead-process
/// races, so calling this at any point is safe.
let stopAllToolRuns () =
    for KeyValue (_, h) in toolRunHandles do h.Stop ()

