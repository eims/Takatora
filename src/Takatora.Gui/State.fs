let init () : Model * Cmd<Msg> =
    { OpenTabs       = [ Home ]
      ActiveTab      = Home
      Projects       = ProjectRegistry.load ()
      ProjectSubTabs = Map.empty
      ProjectHistory = Map.empty
      ProjectFlows   = Map.empty
      ProjectInfo    = Map.empty
      ProjectSecrets = Map.empty
      RunDetails     = Map.empty
      LiveRuns       = Map.empty
      AddProject     = None
      CurrentProject = None
      RunDialog      = None },
    Cmd.none

let private moveOrAppend (tab: RootTab) (tabs: RootTab list) : RootTab list =
    if List.contains tab tabs then tabs else tabs @ [ tab ]

/// Look up the active sub-tab for a project, defaulting to Flows. Used
/// by views; not used by `update` (which writes the map directly).
let projectSubTab (pid: ProjectId) (model: Model) : ProjectSubTab =
    Map.tryFind pid model.ProjectSubTabs
    |> Option.defaultValue ProjectFlows

// ─── Per-project tab scoping ─────────────────────────────────────────
// The root strip used to show every open tab in one flat row, mixing
// projects together. These helpers let the view scope the strip to the
// "current project" (the one owning the active tab) so the user only
// ever sees one project's tabs at a time. None of this lives in the
// model — it's all derived from OpenTabs/ActiveTab, so there's no extra
// state to keep in sync.

/// The project a tab belongs to, if any. Home is global (None). A
/// LiveRun tab's project is read from its live state; an entry always
/// exists while the tab is open (LiveRunCompleted only drops the entry
/// when the tab is also closed), so this is reliable for open tabs.
let tabProject (model: Model) (tab: RootTab) : ProjectId option =
    match tab with
    | Home               -> None
    | Project pid        -> Some pid
    | RunDetail (pid, _) -> Some pid
    | LiveRun key ->
        Map.tryFind key model.LiveRuns
        |> Option.map (fun s -> s.ProjectId)

/// The project context the strip is scoped to. Sticky model state, not
/// derived from the active tab — so clicking Home keeps the project's
/// tabs visible rather than wiping the strip.
let currentProject (model: Model) : ProjectId option =
    model.CurrentProject

/// Distinct open project contexts, in first-seen order across OpenTabs.
/// Drives the project selector's choice list.
let openProjects (model: Model) : ProjectId list =
    model.OpenTabs
    |> List.choose (tabProject model)
    |> List.distinct

/// Tabs the strip should show: Home always (it's a global overlay),
/// plus the tabs belonging to the current project context. With no
/// context (CurrentProject = None) only Home shows.
let visibleTabs (model: Model) : RootTab list =
    let ctx = model.CurrentProject
    model.OpenTabs
    |> List.filter (fun tab ->
        match tab with
        | Home -> true
        | _    -> tabProject model tab = ctx)

/// Stable, human-friendly serial number for a run within its project,
/// derived from chronological order: the oldest run is #1, the newest is
/// #N. `RunHistory.load` returns newest-first, so the number is
/// `count - index`. Stays stable as new runs are appended (existing runs
/// keep their number). None when the run isn't in the cached history.
let runNumber (model: Model) (pid: ProjectId) (rid: RunId) : int option =
    match Map.tryFind pid model.ProjectHistory with
    | Some entries ->
        match List.tryFindIndex (fun (e: RunHistoryEntry) -> e.RunId = rid) entries with
        | Some i -> Some (entries.Length - i)
        | None   -> None
    | None -> None

/// Flow id recorded for a run, looked up from the cached history.
let runFlowId (model: Model) (pid: ProjectId) (rid: RunId) : string option =
    match Map.tryFind pid model.ProjectHistory with
    | Some entries ->
        entries
        |> List.tryFind (fun (e: RunHistoryEntry) -> e.RunId = rid)
        |> Option.map (fun e -> e.FlowId)
    | None -> None

/// The run whose serial number is `offset` away from `rid`'s (e.g. +1 =
/// the next-newer run #N+1, -1 = the next-older #N-1). Drives the
/// prev/next navigation on a RunDetail tab. None at the ends of history
/// or when `rid` isn't in the cached list. History is newest-first, so a
/// higher number sits at a lower index → target index = i - offset.
let runByNumberOffset (model: Model) (pid: ProjectId) (rid: RunId) (offset: int) : RunId option =
    match Map.tryFind pid model.ProjectHistory with
    | Some entries ->
        match List.tryFindIndex (fun (e: RunHistoryEntry) -> e.RunId = rid) entries with
        | Some i ->
            let targetIdx = i - offset
            if targetIdx >= 0 && targetIdx < List.length entries
            then Some (List.item targetIdx entries).RunId
            else None
        | None -> None
    | None -> None

let private projectRoot
        (pid: ProjectId)
        (projects: ProjectRegistration list)
        : string option =
    projects
    |> List.tryFind (fun (p: ProjectRegistration) -> p.Name = pid)
    |> Option.map (fun p -> p.Path)

let private loadHistoryFor
        (pid: ProjectId)
        (projects: ProjectRegistration list)
        : RunHistoryEntry list =
    match projectRoot pid projects with
    | Some root -> RunHistory.load root
    | None      -> []

/// Reload a project's history into the cache, but only if it's already
/// cached — i.e. the Project tab is open (CloseTab drops the entry).
/// Used after a run completes so a visible History sub-tab updates
/// without manual Refresh, while avoiding orphan cache entries for
/// closed projects (which would otherwise defeat OpenProject's lazy
/// reload on the next open).
let private refreshHistoryIfOpen
        (pid: ProjectId)
        (model: Model)
        : Map<ProjectId, RunHistoryEntry list> =
    if Map.containsKey pid model.ProjectHistory then
        Map.add pid (loadHistoryFor pid model.Projects) model.ProjectHistory
    else model.ProjectHistory

/// Read and classify `.ci/flows.toml` for the given project. We keep
/// the three outcomes distinct because each maps to a different UX:
/// missing → "no flows yet, here's how to add one"; error → "your
/// flows.toml has a bug, here's the message"; ok → render the list.
let private loadFlowsFor
        (pid: ProjectId)
        (projects: ProjectRegistration list)
        : FlowsLoad =
    match projectRoot pid projects with
    | None -> FlowsError (sprintf "Project '%s' not in registry" pid)
    | Some root ->
        let path = Path.Combine(root, ".ci", "flows.toml")
        if not (File.Exists path) then FlowsMissing
        else
            try FlowsOk (TomlConfig.loadFlows path)
            with ex -> FlowsError ex.Message

// ─── Run-with-parameters helpers ────────────────────────────────────

/// Var kinds the run dialog can render: scalars (text/pills), path/file/dir
/// (text + native picker), multiline (textarea), secret (masked + keychain),
/// and list (per-item row editor). All VarKinds are now renderable.
let isDialogVarKind (kind: VarKind) : bool =
    match kind with
    | VarKind.String | VarKind.Int | VarKind.Float
    | VarKind.Bool | VarKind.Enum _
    | VarKind.Path | VarKind.File | VarKind.Dir
    | VarKind.Multiline | VarKind.Secret
    | VarKind.List _ -> true

/// Initial editing text for a var's input: its declared default, or a
/// sensible empty/first value when no default is given.
let varDefaultText (v: FlowVar) : string =
    match v.Default with
    | Some (TString s) -> s
    | Some (TInt i)    -> string i
    | Some (TFloat f)  -> Convert.ToString(f, System.Globalization.CultureInfo.InvariantCulture)
    | Some (TBool b)   -> if b then "true" else "false"
    | Some _           -> ""   // array/table — not a scalar kind
    | None ->
        match v.Kind with
        | VarKind.Bool       -> "false"
        | VarKind.Enum (h :: _) -> h
        | _                  -> ""

/// The loaded Flow for a project's flow id, if its flows.toml is cached.
let flowById (model: Model) (pid: ProjectId) (flowId: string) : Flow option =
    match Map.tryFind pid model.ProjectFlows with
    | Some (FlowsOk flows) -> flows |> List.tryFind (fun f -> f.Id = flowId)
    | _ -> None

/// The dialog-renderable vars of a flow (declaration order), or [] if the
/// flow / its flows.toml isn't loaded or has no such vars.
let flowDialogVars (model: Model) (pid: ProjectId) (flowId: string) : FlowVar list =
    match flowById model pid flowId with
    | Some f -> f.Vars |> List.filter (fun v -> isDialogVarKind v.Kind)
    | None   -> []

/// Bool vars that some step's `when` references — surfaced as a top
/// "Toggles" section in the run dialog (they gate whole steps). The MVP
/// `when` grammar is `${vars.X}` / `!${vars.X}`, so a substring match on
/// the `${vars.X}` token is sufficient.
let whenReferencedToggles (flow: Flow) : Set<string> =
    flow.Vars
    |> List.choose (fun v -> if v.Kind = VarKind.Bool then Some v.Name else None)
    |> List.filter (fun name ->
        let token = sprintf "${vars.%s}" name
        flow.Steps |> List.exists (fun s ->
            match s.When with
            | Some w -> w.Contains(token)
            | None   -> false))
    |> Set.ofList

/// Parse a single scalar text into a typed value per `kind`. `label` is
/// used in error messages (the var name, or "<var>[<i>]" for a list item).
let private parseScalarKind (kind: VarKind) (label: string) (text: string) : Result<TomlValue, string> =
    match kind with
    | VarKind.String | VarKind.Multiline | VarKind.Path
    | VarKind.File | VarKind.Dir | VarKind.Secret | VarKind.List _ -> Ok (TString text)
    | VarKind.Bool -> Ok (TBool (text = "true"))
    | VarKind.Enum values ->
        if List.contains text values then Ok (TString text)
        else Error (sprintf "%s: '%s' is not one of %s" label text (String.concat ", " values))
    | VarKind.Int ->
        match Int64.TryParse(text.Trim(),
                             System.Globalization.NumberStyles.Integer,
                             System.Globalization.CultureInfo.InvariantCulture) with
        | true, i -> Ok (TInt i)
        | _ -> Error (sprintf "%s: '%s' is not an integer" label text)
    | VarKind.Float ->
        match Double.TryParse(text.Trim(),
                              System.Globalization.NumberStyles.Float,
                              System.Globalization.CultureInfo.InvariantCulture) with
        | true, f -> Ok (TFloat f)
        | _ -> Error (sprintf "%s: '%s' is not a number" label text)

let private parseOverrideValue (v: FlowVar) (text: string) : Result<TomlValue, string> =
    parseScalarKind v.Kind v.Name text

/// Item texts to pre-fill a list var's editor with: its declared default
/// array rendered element-by-element, or [] if no array default.
let listDefaultTexts (v: FlowVar) : string list =
    match v.Default with
    | Some (TArray xs) ->
        xs |> List.map (function
            | TString s -> s
            | TInt i    -> string i
            | TFloat f  -> Convert.ToString(f, System.Globalization.CultureInfo.InvariantCulture)
            | TBool b   -> if b then "true" else "false"
            | _         -> "")
    | _ -> []

/// Parse a list var's item texts into a TArray. Blank/whitespace rows are
/// dropped (forgiving trailing empties); the rest parse per `itemKind`.
let private parseListItems (label: string) (itemKind: VarKind) (items: string list) : Result<TomlValue, string> =
    let rec go acc = function
        | [] -> Ok (TArray (List.rev acc))
        | (item: string) :: rest ->
            if item.Trim() = "" then go acc rest
            else
                match parseScalarKind itemKind label item with
                | Error e -> Error e
                | Ok v    -> go (v :: acc) rest
    go [] items

/// Parse the non-secret edited fields, keeping only the ones that differ
/// from the declared default (so RunHistory's OverriddenKeys stays honest).
/// Secret vars are excluded here — they have no meaningful "default" diff
/// and their values come from the keychain/dialog, merged at confirm time.
/// Stops at the first parse error.
let buildOverrides (d: RunDialogState) : Result<Map<string, TomlValue>, string> =
    let addIfOverride acc (v: FlowVar) (value: TomlValue) rest go =
        let isOverride =
            match v.Default with
            | Some def -> def <> value
            | None     -> value <> TArray []   // empty list with no default → no override
        go (if isOverride then Map.add v.Name value acc else acc) rest
    let rec go acc vars =
        match vars with
        | [] -> Ok acc
        | (v: FlowVar) :: rest when v.Kind = VarKind.Secret -> go acc rest
        | (v: FlowVar) :: rest ->
            match v.Kind with
            | VarKind.List itemKind ->
                let items = Map.tryFind v.Name d.Lists |> Option.defaultValue []
                match parseListItems v.Name itemKind items with
                | Error e  -> Error e
                | Ok value -> addIfOverride acc v value rest go
            | _ ->
                let text = Map.tryFind v.Name d.Values |> Option.defaultValue (varDefaultText v)
                match parseOverrideValue v text with
                | Error e  -> Error e
                | Ok value -> addIfOverride acc v value rest go
    go Map.empty d.Vars

/// Merge secret values into a base override map for a confirmed dialog,
/// and persist any "remembered" secrets to the keychain. A non-empty
/// secret field becomes an override (kept off disk by the runner's
/// redaction); a blank one is omitted.
let applySecretOverrides
        (d: RunDialogState)
        (overrides: Map<string, TomlValue>)
        : Map<string, TomlValue> =
    d.Vars
    |> List.filter (fun v -> v.Kind = VarKind.Secret)
    |> List.fold (fun acc v ->
        match Map.tryFind v.Name d.Values with
        | Some value when value <> "" ->
            if Set.contains v.Name d.Remember then
                try Secrets.write d.ProjectId v.Name value with _ -> ()
            Map.add v.Name (TString value) acc
        | _ -> acc) overrides

/// Fields the user has changed from their declared default, as
/// (name, defaultText, currentText) for display in the dialog's "Diff
/// from defaults" section. Textual (forgiving — never errors); secret
/// vars are omitted so their value never appears. List values render as
/// "[a, b]" (blank rows dropped, matching confirm).
let dialogDiffs (d: RunDialogState) : (string * string * string) list =
    let shown (s: string) = if s = "" then "(empty)" else s
    let listText (items: string list) =
        sprintf "[%s]" (items |> List.filter (fun s -> s.Trim() <> "") |> String.concat ", ")
    d.Vars
    |> List.filter (fun v -> v.Kind <> VarKind.Secret)
    |> List.choose (fun v ->
        match v.Kind with
        | VarKind.List _ ->
            let def = listDefaultTexts v
            let cur = Map.tryFind v.Name d.Lists |> Option.defaultValue []
            let defClean = def |> List.filter (fun s -> s.Trim() <> "")
            let curClean = cur |> List.filter (fun s -> s.Trim() <> "")
            if defClean <> curClean then Some (v.Name, listText def, listText cur) else None
        | _ ->
            let def = varDefaultText v
            let cur = Map.tryFind v.Name d.Values |> Option.defaultValue def
            if cur <> def then Some (v.Name, shown def, shown cur) else None)

/// Resolve the Tasks SDK assembly + builtin tasks dir from the host
/// process layout. Same defaults the CLI uses; works here because the
/// GUI project-references Takatora.Tasks + Takatora.Tasks.Builtin, so
/// Tasks.dll + builtin-tasks/ get copied next to the GUI executable.
let private sdkAssemblyPath () =
    Path.Combine(AppContext.BaseDirectory, "Takatora.Tasks.dll")

let private builtinTasksDir () =
    Path.Combine(AppContext.BaseDirectory, "builtin-tasks")

// ─── Live run tailing ───────────────────────────────────────────────
//
// The runner writes events.ndjson + log.txt under
// `<root>/.ci/runs/<runId>/` while it executes (each step appends as it
// goes). The GUI never learns the runId — Run.execute generates it
// internally — so we detect the new run dir by diffing `.ci/runs/`
// against a snapshot captured the instant before the run starts, then
// poll that dir on a background thread, marshaling snapshots to the UI.

/// Run-dir names present under `<root>/.ci/runs/` right now. Captured at
/// launch so the poller can spot the one this run creates.
let private existingRunDirs (root: string) : Set<string> =
    let runsDir = Path.Combine(root, ".ci", "runs")
    if Directory.Exists runsDir then
        Directory.GetDirectories runsDir
        |> Array.map Path.GetFileName
        |> Set.ofArray
    else Set.empty

/// The newest run dir under `<root>/.ci/runs/` not present in `before`.
/// Run ids sort lexicographically by time, so the max name is the latest.
let private findNewRunDir (root: string) (before: Set<string>) : string option =
    let runsDir = Path.Combine(root, ".ci", "runs")
    if not (Directory.Exists runsDir) then None
    else
        Directory.GetDirectories runsDir
        |> Array.map Path.GetFileName
        |> Array.filter (fun n -> not (Set.contains n before))
        |> Array.sortDescending
        |> Array.tryHead
        |> Option.map (fun n -> Path.Combine(runsDir, n))

/// Read all non-empty lines from a file the runner may be appending to
/// concurrently. FileShare.ReadWrite avoids fighting the writer's lock.
let private readSharedLines (path: string) : string list =
    try
        if not (File.Exists path) then []
        else
            use fs =
                new FileStream(path, FileMode.Open, FileAccess.Read, FileShare.ReadWrite)
            use sr = new StreamReader(fs)
            sr.ReadToEnd().Split('\n')
            |> Array.choose (fun l ->
                let t = l.TrimEnd('\r')
                if t.Trim() = "" then None else Some t)
            |> Array.toList
    with _ -> []

let private maxLogTail = 300

/// Parse a run dir's events.ndjson + log.txt into a LiveProgress
/// snapshot. Tolerant of partial/garbled trailing lines (the writer may
/// be mid-append) — bad lines are skipped rather than failing the tick.
let private readProgress (runDir: string) : LiveProgress =
    let events = readSharedLines (Path.Combine(runDir, "events.ndjson"))
    // Ordered, de-duplicated step list keyed by step_id.
    let steps = System.Collections.Generic.List<LiveStep>()
    let indexOf (id: string) =
        let mutable found = -1
        for i in 0 .. steps.Count - 1 do
            if steps.[i].Id = id then found <- i
        found
    let upsert (id: string) (typ: string) (status: LiveStepStatus) (dur: float) =
        let i = indexOf id
        let step = { Id = id; Type = typ; Status = status; DurationSec = dur }
        if i < 0 then steps.Add step else steps.[i] <- step
    let mutable ended = false
    for line in events do
        try
            use doc = JsonDocument.Parse(line)
            let root = doc.RootElement
            let str (name: string) =
                match root.TryGetProperty name with
                | true, v when v.ValueKind = JsonValueKind.String -> v.GetString()
                | _ -> ""
            let num (name: string) =
                match root.TryGetProperty name with
                | true, v when v.ValueKind = JsonValueKind.Number -> v.GetDouble()
                | _ -> 0.0
            match str "kind" with
            | "step.start" -> upsert (str "step_id") (str "type") StepRunning 0.0
            | "step.end" ->
                let status =
                    match str "status" with
                    | "success"   -> StepOk
                    | "cancelled" -> StepCancelled
                    | _           -> StepFailed
                upsert (str "step_id") (str "type") status (num "duration_sec")
            | "step.skip" -> upsert (str "step_id") (str "type") StepSkipped 0.0
            | "run.end"   -> ended <- true
            | _ -> ()
        with _ -> ()
    let logTail =
        let all = readSharedLines (Path.Combine(runDir, "log.txt"))
        if List.length all > maxLogTail then
            all |> List.skip (List.length all - maxLogTail)
        else all
    { RunDir = Some runDir; Steps = List.ofSeq steps; LogTail = logTail; Ended = ended }

/// Same shape as `loadFlowsFor` but for `.ci/project.toml`.
let private loadProjectInfoFor
        (pid: ProjectId)
        (projects: ProjectRegistration list)
        : ProjectInfoLoad =
    match projectRoot pid projects with
    | None -> ProjectInfoError (sprintf "Project '%s' not in registry" pid)
    | Some root ->
        let path = Path.Combine(root, ".ci", "project.toml")
        if not (File.Exists path) then ProjectInfoMissing
        else
            try ProjectInfoOk (TomlConfig.loadProject path)
            with ex -> ProjectInfoError ex.Message

/// Tabs that *bulk* close operations may remove: closable run tabs only.
/// Home and the project root are anchors — bulk ops never touch them.
let bulkClosable (tab: RootTab) : bool =
    match tab with
    | Home | Project _        -> false
    | RunDetail _ | LiveRun _ -> true

/// Remove `toClose` from the model in one pass: drop the per-tab caches,
/// recompute the active tab within the current context (keeping the same
/// strip position where possible), and fall back to Home/no-context if
/// the current project has no tabs left. Home is never closed. Shared by
/// CloseTab and the bulk close handlers so they all reassign consistently.
let closeTabs (toClose: RootTab list) (model: Model) : Model * Cmd<Msg> =
    let toCloseSet =
        toClose |> List.filter (function Home -> false | _ -> true) |> Set.ofList
    if Set.isEmpty toCloseSet then model, Cmd.none
    else
        let cleanCaches (m: Model) (t: RootTab) : Model =
            match t with
            | Project pid ->
                { m with
                    ProjectSubTabs = Map.remove pid m.ProjectSubTabs
                    ProjectHistory = Map.remove pid m.ProjectHistory
                    ProjectFlows   = Map.remove pid m.ProjectFlows
                    ProjectInfo    = Map.remove pid m.ProjectInfo }
            | RunDetail (pid, rid) -> { m with RunDetails = Map.remove (pid, rid) m.RunDetails }
            | LiveRun _ | Home     -> m
        // Pick the next active tab within the strip the user is looking at,
        // keeping the same slot index where possible (survivors always
        // include Home). LiveRuns entries are intentionally NOT dropped for
        // still-Pending tabs — LiveRunCompleted needs them to find the pid.
        let oldVisible = visibleTabs model
        let newActive =
            if not (Set.contains model.ActiveTab toCloseSet) then model.ActiveTab
            else
                match List.tryFindIndex ((=) model.ActiveTab) oldVisible with
                | None -> Home
                | Some idx ->
                    let survivors =
                        oldVisible |> List.filter (fun t -> not (Set.contains t toCloseSet))
                    if List.isEmpty survivors then Home
                    elif idx < List.length survivors then List.item idx survivors
                    else List.last survivors
        let cleaned = Set.fold cleanCaches model toCloseSet
        let intermediate =
            { cleaned with
                OpenTabs  = model.OpenTabs |> List.filter (fun t -> not (Set.contains t toCloseSet))
                ActiveTab = newActive }
        let contextStillOpen =
            match model.CurrentProject with
            | Some pid -> openProjects intermediate |> List.contains pid
            | None     -> false
        if contextStillOpen then intermediate, Cmd.none
        else { intermediate with ActiveTab = Home; CurrentProject = None }, Cmd.none

/// Kick off an in-process flow run with the given var overrides: spin up
/// a LiveRun tab immediately, then schedule Run.execute + the tail poller
/// on the thread pool (marshaling results back via Dispatcher.UIThread).
/// Shared by the no-vars Run path and the param-dialog confirm.
let private startRun
        (pid: ProjectId)
        (flowId: string)
        (overrides: Map<string, TomlValue>)
        (model: Model)
        : Model * Cmd<Msg> =
    match projectRoot pid model.Projects with
    | None -> model, Cmd.none
    | Some root ->
        let key = Guid.NewGuid()
        let tab = LiveRun key
        let liveState = {
            ProjectId = pid
            FlowId    = flowId
            StartedAt = DateTimeOffset.UtcNow
            Phase     = LivePending
            Progress  = LiveProgress.empty
            CancelRequested = false
        }
        // Snapshot the runs dir before launching so the poller can
        // identify the dir this run is about to create.
        let runsBefore = existingRunDirs root
        let opts : Run.Options = {
            WorkingDir       = root
            FlowId           = flowId
            VarOverrides     = overrides
            SdkAssemblyPath  = sdkAssemblyPath ()
            BuiltinTasksDir  = builtinTasksDir ()
            UserTasksDir     = None
        }
        // Run on the thread pool, then marshal the result message
        // back onto the Avalonia UI thread before dispatching.
        //
        // We deliberately do NOT use Cmd.OfAsync.either here: in
        // Elmish, the dispatch loop runs `setState` (→ FuncUI's
        // host.Update → Avalonia view rebuild) on whatever thread
        // calls `dispatch`. FuncUI.Elmish 1.5.1's withHost does not
        // marshal, so dispatching from the thread-pool thread would
        // rebuild the view off the UI thread and crash the whole
        // app. Dispatcher.UIThread.Post guarantees the dispatch —
        // and thus the re-render — happens on the UI thread.
        let runCmd : Cmd<Msg> =
            [ fun dispatch ->
                async {
                    do! Async.SwitchToThreadPool ()
                    let msg =
                        try LiveRunCompleted (key, Run.execute opts)
                        with ex -> LiveRunFailed (key, ex)
                    Dispatcher.UIThread.Post(fun () -> dispatch msg)
                }
                |> Async.Start ]
        // Independent tail poller. Reads the run's events/log files
        // (which Run.execute appends to live) every ~300ms and posts
        // a LiveRunProgress snapshot to the UI thread, until it sees
        // a run.end event or hits the safety cap. Runs concurrently
        // with the executor above; both are UI-thread-safe via Post.
        let pollCmd : Cmd<Msg> =
            [ fun dispatch ->
                async {
                    do! Async.SwitchToThreadPool ()
                    let mutable runDir = None
                    let mutable finished = false
                    let mutable ticks = 0
                    // Cap ~ 1 hour at 300ms cadence; a stuck run won't
                    // poll forever if run.end is somehow never written.
                    while not finished && ticks < 12000 do
                        ticks <- ticks + 1
                        if Option.isNone runDir then
                            runDir <- findNewRunDir root runsBefore
                        match runDir with
                        | Some dir ->
                            let progress = readProgress dir
                            Dispatcher.UIThread.Post(fun () ->
                                dispatch (LiveRunProgress (key, progress)))
                            if progress.Ended then finished <- true
                        | None -> ()
                        if not finished then do! Async.Sleep 300
                }
                |> Async.Start ]
        { model with
            OpenTabs       = moveOrAppend tab model.OpenTabs
            ActiveTab      = tab
            CurrentProject = Some pid
            LiveRuns       = Map.add key liveState model.LiveRuns },
        Cmd.batch [ runCmd; pollCmd ]

let update (msg: Msg) (model: Model) : Model * Cmd<Msg> =
    match msg with
    | RefreshProjects ->
        { model with Projects = ProjectRegistry.load () }, Cmd.none
    | OpenProject pid ->
        let tab = Project pid
        let history =
            if Map.containsKey pid model.ProjectHistory then model.ProjectHistory
            else Map.add pid (loadHistoryFor pid model.Projects) model.ProjectHistory
        let flows =
            if Map.containsKey pid model.ProjectFlows then model.ProjectFlows
            else Map.add pid (loadFlowsFor pid model.Projects) model.ProjectFlows
        let info =
            if Map.containsKey pid model.ProjectInfo then model.ProjectInfo
            else Map.add pid (loadProjectInfoFor pid model.Projects) model.ProjectInfo
        let secrets =
            if Map.containsKey pid model.ProjectSecrets then model.ProjectSecrets
            else Map.add pid (Secrets.listForProject pid) model.ProjectSecrets
        { model with
            OpenTabs       = moveOrAppend tab model.OpenTabs
            ActiveTab      = tab
            CurrentProject = Some pid
            ProjectHistory = history
            ProjectFlows   = flows
            ProjectInfo    = info
            ProjectSecrets = secrets },
        Cmd.none
    | ActivateTab tab ->
        if List.contains tab model.OpenTabs then
            // Activating a project-owning tab switches the context to it;
            // activating Home leaves CurrentProject sticky so the strip
            // keeps showing the project's tabs.
            let current =
                match tabProject model tab with
                | Some pid -> Some pid
                | None     -> model.CurrentProject
            { model with ActiveTab = tab; CurrentProject = current }, Cmd.none
        else model, Cmd.none
    | CloseTab Home -> model, Cmd.none  // Home is not closable.
    | CloseTab tab -> closeTabs [ tab ] model
    | CloseOtherTabs keep ->
        // Close every other run tab in this context; keep Home, the project
        // root, and `keep` (which also becomes the active tab).
        let toClose =
            visibleTabs model
            |> List.filter (fun t -> bulkClosable t && t <> keep)
        let m, cmd = closeTabs toClose model
        let current =
            match tabProject m keep with
            | Some pid -> Some pid
            | None     -> m.CurrentProject
        { m with ActiveTab = keep; CurrentProject = current }, cmd
    | CloseTabsToRight tab ->
        // Close the closable tabs that come after `tab` in the visible strip.
        let vis = visibleTabs model
        match List.tryFindIndex ((=) tab) vis with
        | None -> model, Cmd.none
        | Some idx ->
            let toClose =
                vis
                |> List.indexed
                |> List.choose (fun (i, t) ->
                    if i > idx && bulkClosable t then Some t else None)
            closeTabs toClose model
    | ActivateSubTab (pid, sub) ->
        // Entering Settings re-reads the keychain so a secret saved via the
        // run dialog (or removed in the OS) shows up without a manual refresh.
        let secrets =
            match sub with
            | ProjectSettings -> Map.add pid (Secrets.listForProject pid) model.ProjectSecrets
            | _               -> model.ProjectSecrets
        { model with
            ProjectSubTabs = Map.add pid sub model.ProjectSubTabs
            ProjectSecrets = secrets },
        Cmd.none
    | RefreshHistory pid ->
        { model with
            ProjectHistory = Map.add pid (loadHistoryFor pid model.Projects) model.ProjectHistory },
        Cmd.none
    | RefreshFlows pid ->
        { model with
            ProjectFlows = Map.add pid (loadFlowsFor pid model.Projects) model.ProjectFlows },
        Cmd.none
    | RefreshProjectInfo pid ->
        { model with
            ProjectInfo    = Map.add pid (loadProjectInfoFor pid model.Projects) model.ProjectInfo
            ProjectSecrets = Map.add pid (Secrets.listForProject pid) model.ProjectSecrets },
        Cmd.none
    | OpenRunDetail (pid, runId) ->
        let tab = RunDetail (pid, runId)
        let key = pid, runId
        let details =
            if Map.containsKey key model.RunDetails then model.RunDetails
            else
                match projectRoot pid model.Projects with
                | None -> model.RunDetails
                | Some root ->
                    match RunHistory.findRun root runId with
                    | Some data -> Map.add key data model.RunDetails
                    | None      -> model.RunDetails
        { model with
            OpenTabs       = moveOrAppend tab model.OpenTabs
            ActiveTab      = tab
            CurrentProject = Some pid
            RunDetails     = details },
        Cmd.none
    | RequestRun (pid, flowId) ->
        // Run button entry point. If the flow declares scalar vars, open
        // the param dialog (pre-filled with declared defaults); otherwise
        // there's nothing to fill in, so run straight away.
        match flowDialogVars model pid flowId with
        | [] -> startRun pid flowId Map.empty model
        | vars ->
            // Secret fields pre-fill from the keychain (shown masked); a
            // stored secret defaults to "remembered" so a re-run keeps it.
            let stored =
                vars
                |> List.filter (fun v -> v.Kind = VarKind.Secret && Secrets.exists pid v.Name)
                |> List.map (fun v -> v.Name)
                |> Set.ofList
            let values =
                vars
                |> List.map (fun v ->
                    match v.Kind with
                    | VarKind.Secret -> v.Name, (Secrets.read pid v.Name |> Option.defaultValue "")
                    | _              -> v.Name, varDefaultText v)
                |> Map.ofList
            let lists =
                vars
                |> List.choose (fun v ->
                    match v.Kind with
                    | VarKind.List _ -> Some (v.Name, listDefaultTexts v)
                    | _              -> None)
                |> Map.ofList
            let toggles =
                match flowById model pid flowId with
                | Some f -> whenReferencedToggles f
                | None   -> Set.empty
            { model with
                RunDialog =
                    Some { ProjectId = pid
                           FlowId    = flowId
                           Vars      = vars
                           Values    = values
                           Lists     = lists
                           Toggles   = toggles
                           Remember  = stored
                           Stored    = stored
                           Error     = None } },
            Cmd.none
    | RunDialogSetValue (name, value) ->
        match model.RunDialog with
        | None -> model, Cmd.none
        | Some d ->
            { model with
                RunDialog =
                    Some { d with
                            Values = Map.add name value d.Values
                            Error  = None } },
            Cmd.none
    | RunDialogToggleRemember name ->
        match model.RunDialog with
        | None -> model, Cmd.none
        | Some d ->
            let remember =
                if Set.contains name d.Remember then Set.remove name d.Remember
                else Set.add name d.Remember
            { model with RunDialog = Some { d with Remember = remember } }, Cmd.none
    | RunDialogForget name ->
        // Drop the stored keychain entry and clear the field.
        match model.RunDialog with
        | None -> model, Cmd.none
        | Some d ->
            Secrets.delete d.ProjectId name |> ignore
            { model with
                RunDialog =
                    Some { d with
                            Values   = Map.add name "" d.Values
                            Stored   = Set.remove name d.Stored
                            Remember = Set.remove name d.Remember } },
            Cmd.none
    | RunDialogListAdd name ->
        match model.RunDialog with
        | None -> model, Cmd.none
        | Some d ->
            let cur = Map.tryFind name d.Lists |> Option.defaultValue []
            { model with RunDialog = Some { d with Lists = Map.add name (cur @ [ "" ]) d.Lists; Error = None } },
            Cmd.none
    | RunDialogListRemove (name, index) ->
        match model.RunDialog with
        | None -> model, Cmd.none
        | Some d ->
            let cur = Map.tryFind name d.Lists |> Option.defaultValue []
            let next = cur |> List.indexed |> List.choose (fun (i, x) -> if i = index then None else Some x)
            { model with RunDialog = Some { d with Lists = Map.add name next d.Lists; Error = None } },
            Cmd.none
    | RunDialogListSetItem (name, index, value) ->
        match model.RunDialog with
        | None -> model, Cmd.none
        | Some d ->
            let cur = Map.tryFind name d.Lists |> Option.defaultValue []
            let next = cur |> List.mapi (fun i x -> if i = index then value else x)
            { model with RunDialog = Some { d with Lists = Map.add name next d.Lists; Error = None } },
            Cmd.none
    | RunDialogReset ->
        // Restore every field to its declared default (secrets re-read the
        // keychain so a stored value reappears; lists reset to their array).
        match model.RunDialog with
        | None -> model, Cmd.none
        | Some d ->
            let values =
                d.Vars
                |> List.map (fun v ->
                    match v.Kind with
                    | VarKind.Secret -> v.Name, (Secrets.read d.ProjectId v.Name |> Option.defaultValue "")
                    | _              -> v.Name, varDefaultText v)
                |> Map.ofList
            let lists =
                d.Vars
                |> List.choose (fun v ->
                    match v.Kind with
                    | VarKind.List _ -> Some (v.Name, listDefaultTexts v)
                    | _              -> None)
                |> Map.ofList
            { model with RunDialog = Some { d with Values = values; Lists = lists; Error = None } },
            Cmd.none
    | RunDialogCancel ->
        { model with RunDialog = None }, Cmd.none
    | RunDialogConfirm ->
        match model.RunDialog with
        | None -> model, Cmd.none
        | Some d ->
            match buildOverrides d with
            | Error msg ->
                { model with RunDialog = Some { d with Error = Some msg } }, Cmd.none
            | Ok overrides ->
                // Merge in secret values (and persist any "remembered" ones
                // to the keychain). Clear the dialog first so startRun's
                // model carries the closed state regardless of the run.
                let overrides = applySecretOverrides d overrides
                startRun d.ProjectId d.FlowId overrides { model with RunDialog = None }
    | RunFlow (pid, flowId) ->
        // Direct run with no overrides (used internally / by RequestRun
        // for flows that declare no scalar vars).
        startRun pid flowId Map.empty model
    | OpenInExplorer path ->
        // Open the directory in the OS file explorer. Best-effort; a missing
        // dir or a headless host just no-ops.
        (try
            if Directory.Exists path then
                let psi = System.Diagnostics.ProcessStartInfo("explorer.exe")
                psi.ArgumentList.Add(path)
                System.Diagnostics.Process.Start(psi) |> ignore
         with _ -> ())
        model, Cmd.none
    | DeleteSecret (pid, varName) ->
        // Delete from the keychain, then refresh the cached list so the
        // model changes and the Settings view re-renders without it.
        Secrets.delete pid varName |> ignore
        { model with
            ProjectSecrets = Map.add pid (Secrets.listForProject pid) model.ProjectSecrets },
        Cmd.none
    | LiveRunCompleted (key, result) ->
        match Map.tryFind key model.LiveRuns with
        | None -> model, Cmd.none  // should not happen; guard.
        | Some state ->
            let pid = state.ProjectId
            // Refresh history only if the Project is open (cached). If it's
            // closed, leave the cache untouched so we don't leave an orphan
            // entry that would short-circuit the lazy reload on next OpenProject.
            let newHistory = refreshHistoryIfOpen pid model
            // Keep the LiveRuns entry around if the tab is still open
            // (so the view can render the Completed phase); otherwise
            // drop it to bound the map.
            let newLiveRuns =
                if List.contains (LiveRun key) model.OpenTabs then
                    Map.add key { state with Phase = LiveCompleted result } model.LiveRuns
                else
                    Map.remove key model.LiveRuns
            { model with
                LiveRuns       = newLiveRuns
                ProjectHistory = newHistory },
            Cmd.none
    | LiveRunProgress (key, progress) ->
        // Merge a poller snapshot into the live state's Progress. Never
        // touches Phase, so it can't regress a Completed run back to
        // Pending; updating Progress even after completion just ensures
        // the final step states land if the last poller tick races the
        // executor's LiveRunCompleted. No-op if the tab is gone.
        match Map.tryFind key model.LiveRuns with
        | Some state ->
            { model with
                LiveRuns = Map.add key { state with Progress = progress } model.LiveRuns },
            Cmd.none
        | None -> model, Cmd.none
    | CancelLiveRun key ->
        // Drop a CANCEL flag in the run dir; the runner polls for it
        // (200ms) and kills the active step's process tree, ending the
        // run as Cancelled. Only actionable once the poller has matched
        // the run dir and while the run is still Pending.
        match Map.tryFind key model.LiveRuns with
        | Some state when (match state.Phase with LivePending -> true | _ -> false) ->
            match state.Progress.RunDir with
            | Some dir ->
                try File.WriteAllText(Path.Combine(dir, "CANCEL"), "") with _ -> ()
                { model with
                    LiveRuns =
                        Map.add key { state with CancelRequested = true } model.LiveRuns },
                Cmd.none
            | None -> model, Cmd.none
        | _ -> model, Cmd.none
    | ShowAddProject ->
        { model with
            AddProject =
                Some { Dir = ""; Name = ""; Engine = EngineKind.Unreal; Error = None } },
        Cmd.none
    | HideAddProject ->
        { model with AddProject = None }, Cmd.none
    | AddProjectSetDir dir ->
        match model.AddProject with
        | None -> model, Cmd.none
        | Some f ->
            { model with AddProject = Some { f with Dir = dir; Error = None } }, Cmd.none
    | AddProjectSetName name ->
        match model.AddProject with
        | None -> model, Cmd.none
        | Some f ->
            { model with AddProject = Some { f with Name = name; Error = None } }, Cmd.none
    | AddProjectSetEngine engine ->
        match model.AddProject with
        | None -> model, Cmd.none
        | Some f ->
            { model with AddProject = Some { f with Engine = engine; Error = None } }, Cmd.none
    | SubmitAddProject ->
        match model.AddProject with
        | None -> model, Cmd.none
        | Some form ->
            let withError msg =
                { model with AddProject = Some { form with Error = Some msg } }, Cmd.none
            let dir = form.Dir.Trim()
            if String.IsNullOrWhiteSpace dir then
                withError "Directory path is required."
            else
                try
                    // Scaffold the dir + .ci/ (project.toml + a runnable
                    // starter flow), then register. Creating the dir is the
                    // whole point of the wizard — it removes the CLI's
                    // "directory does not exist" / "no .ci/project.toml"
                    // friction. Existing files are never clobbered. Shared
                    // with the CLI `init` command via Core's Scaffold.
                    let absDir = Path.GetFullPath dir
                    let name =
                        if String.IsNullOrWhiteSpace form.Name then
                            Path.GetFileName(absDir.TrimEnd('\\', '/'))
                        else form.Name.Trim()
                    Scaffold.writeCi absDir name form.Engine |> ignore
                    match ProjectRegistry.add absDir (Some name) with
                    | ProjectRegistry.Added entry ->
                        // Refresh the registry-backed list and open the new
                        // project tab so the user lands somewhere useful.
                        { model with
                            AddProject = None
                            Projects   = ProjectRegistry.load () },
                        Cmd.ofMsg (OpenProject entry.Name)
                    | ProjectRegistry.DuplicateName existing ->
                        withError
                            (sprintf "A project named '%s' is already registered (%s)."
                                existing.Name existing.Path)
                    | ProjectRegistry.InvalidPath reason ->
                        withError reason
                with ex ->
                    withError ex.Message
    | LiveRunFailed (key, ex) ->
        // Unexpected exception from the async machinery itself (not a
        // RunFailure, which is already a Result Error). Synthesize an
        // InternalError so the UI can show something rather than just
        // freezing in Pending.
        match Map.tryFind key model.LiveRuns with
        | None -> model, Cmd.none
        | Some state ->
            let result = Error (RunFailure.InternalError ex.Message)
            let pid = state.ProjectId
            let newHistory = refreshHistoryIfOpen pid model
            let newLiveRuns =
                if List.contains (LiveRun key) model.OpenTabs then
                    Map.add key { state with Phase = LiveCompleted result } model.LiveRuns
                else
                    Map.remove key model.LiveRuns
            { model with
                LiveRuns       = newLiveRuns
                ProjectHistory = newHistory },
            Cmd.none
