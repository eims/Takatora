module Takatora.Gui.State

open System
open System.IO
open System.Text.Json
open Elmish
open Avalonia.Threading
open Takatora.Core

/// Project lookup key inside the GUI. The registry uses `Name` as the
/// stable identifier (per ProjectRegistry.find docs), so we mirror that
/// rather than introducing a separate id type.
type ProjectId = string

/// Run id (mirrors RunHistoryEntry.RunId). Carried alongside ProjectId
/// in RunDetail tabs because RunHistory.findRun needs the project's
/// working dir to locate the run.
type RunId = string

/// Client-side identifier for an in-flight LiveRun. Generated by the
/// GUI at RunFlow time and used as the LiveRun tab's key, because the
/// runner's RunId isn't known until Run.execute returns.
type LiveRunKey = Guid

/// State of one LiveRun tab. Captures enough to render before, during,
/// and after the run completes, without inventing a third "Cancelled
/// by tab close" state — closing the tab doesn't cancel the run, just
/// stops the GUI from observing it.
type LiveRunPhase =
    | LivePending
    | LiveCompleted of Result<RunOutcome, RunFailure>

/// Status of a single step as observed live from events.ndjson. Distinct
/// from Core.StepStatus because mid-run we have a "still running" state
/// the persisted record never has.
type LiveStepStatus =
    | StepRunning
    | StepOk
    | StepFailed
    | StepSkipped
    | StepCancelled

type LiveStep = {
    Id: string
    Type: string
    Status: LiveStepStatus
    DurationSec: float
}

/// A snapshot parsed from a run's events.ndjson + log.txt by the live
/// poller. Replaces the LiveRunState's previous snapshot wholesale on
/// each tick (cheap — these files are tiny).
type LiveProgress = {
    /// Discovered run dir, once the poller has matched it. None until then.
    RunDir: string option
    Steps: LiveStep list
    /// Tail of log.txt (most recent lines, capped).
    LogTail: string list
    /// True once a `run.end` event has been observed.
    Ended: bool
}

module LiveProgress =
    let empty = { RunDir = None; Steps = []; LogTail = []; Ended = false }

type LiveRunState = {
    ProjectId: ProjectId
    FlowId: string
    StartedAt: DateTimeOffset
    Phase: LiveRunPhase
    /// Latest live snapshot from the poller (steps + log tail).
    Progress: LiveProgress
    /// Set once the user clicks Cancel (we've written the CANCEL flag).
    /// Drives the button's "Cancelling…" / disabled state; the run keeps
    /// going until the runner observes the flag and tears down.
    CancelRequested: bool
}

/// A project's active "watch": auto-run FlowId when the git HEAD moves.
/// LastHead is the commit we last acted on (the poll fires when HEAD differs).
/// Enabled is the per-project on/off — false pauses this project's watch while
/// keeping which flow it watches, so it can be resumed without reconfiguring.
type WatchInfo = {
    FlowId: string
    LastHead: string option
    Enabled: bool
}

/// Root tab variants. Home + Project + RunDetail + LiveRun are wired;
/// Settings (the app-wide one from gui.md) will land later.
type RootTab =
    | Home
    | Project of ProjectId
    | RunDetail of ProjectId * RunId
    | LiveRun of LiveRunKey

/// Sub-tabs inside a Project tab. Per gui.md these are plain TabControl
/// territory (only the root strip is custom), but we keep the active
/// sub-tab in the model so re-focusing a Project tab restores its
/// position rather than resetting to Flows.
type ProjectSubTab =
    | ProjectFlows
    | ProjectToolboxTab
    | ProjectHistory
    | ProjectSettings

/// Result of scanning a project's toolbox script dirs. `ToolboxLoading`
/// while the (off-thread) scan is in flight; `ToolboxOk` holds the parsed
/// config + discovered scripts. Mirrors FlowsLoad's role for the cache.
type ToolboxLoad =
    | ToolboxLoading
    | ToolboxOk of ToolboxConfig * ToolEntry list

/// Result of trying to read a project's `.takatora/flows.toml`. The three
/// outcomes need distinct UX — missing file is a setup hint, parse
/// failure is a config bug to surface, success is the happy path.
type FlowsLoad =
    | FlowsOk of Flow list
    | FlowsMissing
    | FlowsError of string

/// Add-Project wizard form state. Present (Some) when the wizard panel
/// on Home is open; None when collapsed. Kept in the model because
/// Elmish owns all UI state — there's no local widget state to lean on.
type AddProjectForm = {
    Dir: string
    Name: string
    Engine: EngineKind
    /// Last submission error, surfaced inline under the form. Cleared on
    /// the next edit/submit so a fixed mistake doesn't keep nagging.
    Error: string option
}

/// "Run with parameters" dialog state. Present (Some) while the modal
/// overlay is open. `Values` holds the raw editing text per var name
/// (bool as "true"/"false", enum as the selected value); parsing into
/// typed `VarOverrides` happens at confirm so partial numeric input
/// doesn't fight the user mid-keystroke.
type RunDialogState = {
    ProjectId: ProjectId
    FlowId: string
    /// Dialog-renderable vars (see State.isDialogVarKind), declaration order.
    Vars: FlowVar list
    Values: Map<string, string>
    /// Per-list-var item texts (raw, one per array element). Parsed into a
    /// TArray per the list's item kind at confirm.
    Lists: Map<string, string list>
    /// Bool var names referenced by a step's `when` — rendered in a top
    /// "Toggles" section (the rest go under "Parameters").
    Toggles: Set<string>
    /// "Save as new default" — write the changed values back to flows.toml
    /// on Run. Off by default.
    SaveDefaults: bool
    /// Secret var names the user has flagged to persist to the keychain on
    /// Run. Initialised on: already-stored secrets default to remembered.
    Remember: Set<string>
    /// Secret var names that currently have a keychain entry (drives the
    /// "saved ✓" / Forget affordance). Updated when Forget fires.
    Stored: Set<string>
    /// Validation error from the last confirm attempt, cleared on edit.
    Error: string option
}

/// "Allow secret access?" overlay state. Opened by startRun when the
/// chosen flow references secret project params without a current
/// machine-local grant; Approve records the grants and re-enters the run.
type GrantPromptState = {
    ProjectId: ProjectId
    FlowId: string
    /// (param name, optional description) — every secret param the flow
    /// references, granted or not, so the dialog shows the full picture.
    Params: (string * string option) list
    /// Names whose grant exists but is stale (the flow changed since).
    Stale: Set<string>
    /// Var overrides of the interrupted run, replayed on Approve.
    Overrides: Map<string, TomlValue>
}

/// Same shape as FlowsLoad but for `.takatora/project.toml`. Project info
/// is technically validated at `project add` time, but the file may
/// have been edited or deleted between registration and now.
type ProjectInfoLoad =
    | ProjectInfoOk of Project
    | ProjectInfoMissing
    | ProjectInfoError of string

type Model = {
    OpenTabs: RootTab list
    ActiveTab: RootTab
    Projects: ProjectRegistration list
    /// Per-Project-tab active sub-tab. Missing key means "default" (Flows).
    ProjectSubTabs: Map<ProjectId, ProjectSubTab>
    /// Cached `RunHistory.load` results, keyed by project id. Loaded
    /// lazily on OpenProject so initial app startup doesn't pay for
    /// every project's runs dir.
    ProjectHistory: Map<ProjectId, RunHistoryEntry list>
    /// Cached `TomlConfig.loadFlows` results, keyed by project id.
    /// Loaded on OpenProject alongside history.
    ProjectFlows: Map<ProjectId, FlowsLoad>
    /// Cached `TomlConfig.loadProject` results, keyed by project id.
    /// Loaded on OpenProject alongside flows.
    ProjectInfo: Map<ProjectId, ProjectInfoLoad>
    /// Engine kind per registered project (from project.toml's [engine].type),
    /// loaded eagerly whenever the project list changes so the Home list can
    /// tint each project name by engine — cheap (a small TOML parse, no runs
    /// scan). Missing/unparseable projects are simply absent (no tint).
    ProjectEngines: Map<ProjectId, EngineKind>
    /// Cached keychain secret var names per project (for the Settings
    /// manager). Refreshed when Settings is opened/refreshed and on
    /// delete, so the list stays a pure function of model state.
    ProjectSecrets: Map<ProjectId, string list>
    /// Cached toolbox scan (config + discovered scripts) per project.
    /// Loaded lazily when the Toolbox sub-tab is first opened; dropped on
    /// tab close so a reopen rescans (picks up scripts added on disk).
    ProjectToolbox: Map<ProjectId, ToolboxLoad>
    /// Machine-local toolbox state (ON/OFF toggles + sort order) per
    /// project. Loaded alongside the scan; edits persist to state.toml.
    ToolboxStates: Map<ProjectId, ToolboxLocalState>
    /// Recent toolbox run history per project (newest first, capped).
    /// Kept separate from ProjectHistory so tool runs never mix into the
    /// flow (Job) history.
    ToolboxHistories: Map<ProjectId, ToolRunRecord list>
    /// Tools with a run in flight (per project + tool key). Guards against
    /// double-launch and drives the "Running…" affordance.
    ToolRunsInFlight: Set<ProjectId * string>
    /// In-flight tool runs the user asked to stop: the kill has been
    /// signalled but the process hasn't been observed dead yet. Drives the
    /// button's disabled "Stopping…" state; cleared by ToolRunCompleted.
    ToolStopsRequested: Set<ProjectId * string>
    /// Last run error per (project, tool key), shown inline on the row.
    /// Cleared when that tool is run again.
    ToolRunErrors: Map<ProjectId * string, string>
    /// The Settings "add directory" textbox draft, per project.
    ToolboxDirDraft: Map<ProjectId, string>
    /// Projects whose Toolbox "Recent runs" section is expanded.
    ToolboxRecentOpen: Set<ProjectId>
    /// Cached `RunHistory.findRun` results for open RunDetail tabs,
    /// keyed by (project, run id). Loaded on OpenRunDetail, dropped
    /// when the corresponding tab closes. Tuple: entry, step summaries,
    /// step outputs (step id → name → value), and the run's full log.txt
    /// lines (so a past run is reviewable in RunDetail).
    RunDetails: Map<ProjectId * RunId, RunHistoryEntry * StepSummary list * Map<string, Map<string, string>> * string list>
    /// Per-RunDetail log search query (a find — jumps to matches, does not
    /// filter) and the current match index (the view wraps it mod count).
    RunLogFilter: Map<ProjectId * RunId, string>
    RunLogMatchIdx: Map<ProjectId * RunId, int>
    /// Projects with an "Open in editor" launch in flight. Opening goes
    /// through the OS shell and the target app (UE editor / Rider) can take
    /// tens of seconds to show a window; without feedback the user re-clicks,
    /// queueing several launches that all open at once. While a project is in
    /// this set its button is disabled ("Opening…") and repeat clicks are
    /// ignored; a timed ClearOpeningEditor removes it.
    OpeningEditors: Set<ProjectId>
    /// Same single-flight guard for "Open in IDE" launches.
    OpeningIde: Set<ProjectId>
    /// Runs whose "Copy CLI command" button was just clicked, for transient
    /// "Copied ✓" feedback. A timed ClearRunCommandCopied removes the entry.
    CopiedRunCmd: Set<ProjectId * RunId>
    /// Machine-local app settings (the "Open in IDE" command template lives
    /// here). Loaded at startup, rewritten by the Settings editor.
    AppSettings: AppSettings
    /// In-progress text of the global IDE-command editor (Settings). Seeded
    /// from AppSettings.IdeCommand; persisted on Save.
    IdeCommandDraft: string
    /// Detected IDE presets (populated on demand by the Settings "Detect"
    /// button) the user can one-click into the command template.
    IdeCandidates: IdeCandidate list
    /// In-progress text of the Godot search-paths editor (one dir per line).
    GodotSearchPathsDraft: string
    /// Detected Godot executables (PATH + configured search paths), for the
    /// user to pick the machine-level Godot from.
    GodotCandidates: DetectedEngine list
    /// The flow step whose describe-schema Inspector is open: (project, flow
    /// id, step index). None = no Inspector.
    SelectedStep: (ProjectId * string * int) option
    /// Describe schema cache, keyed by `stepSchemaKey` (task path + mtime).
    StepSchemas: Map<string, Result<DescribeSchema, string>>
    /// Schema keys with a describe spawn in flight (shows "inspecting…").
    StepSchemasLoading: Set<string>
    /// In-progress "add step" task-type text, keyed by (project, flow id).
    AddStepDraft: Map<ProjectId * string, string>
    /// Flow cards whose step list is expanded (collapsed by default — keeps
    /// the Flows tab low-noise; steps + editing live behind the expander).
    ExpandedFlows: Set<ProjectId * string>
    /// Expanded flows currently in edit mode (step move/delete/add controls).
    EditingFlows: Set<ProjectId * string>
    /// Settings sub-tab search query (filters setting rows/sections by text).
    SettingsFilter: string
    /// Projects being watched (auto-run a flow on new commits), keyed by
    /// project id. One watched flow per project. Runtime-only (not persisted).
    Watches: Map<ProjectId, WatchInfo>
    /// Global watch master. A watch fires only when this is on AND the
    /// project's watch is Enabled AND a flow is watched (global ∧ project ∧
    /// flow). Lets the user pause all watching at once (e.g. from the tray).
    WatchEnabled: bool
    /// Live state of every LiveRun tab the user has kicked off. Entries
    /// are added by RunFlow and removed by LiveRunCompleted (which also
    /// drops the entry if its tab has been closed).
    LiveRuns: Map<LiveRunKey, LiveRunState>
    /// Add-Project wizard state. None = panel collapsed; Some = open with
    /// in-progress field values. Lives on the Home tab.
    AddProject: AddProjectForm option
    /// The project whose tabs the root strip is currently scoped to.
    /// Sticky: opening/activating a project's tab sets it, but clicking
    /// Home does NOT clear it (Home is a global overlay, not a context),
    /// so the project's tabs stay in the strip. None until the first
    /// project is opened.
    CurrentProject: ProjectId option
    /// "Run with parameters" modal state. None = closed. Opened by
    /// RequestRun when the chosen flow declares scalar vars.
    RunDialog: RunDialogState option
    /// True while the About overlay is open.
    ShowingAbout: bool
    /// True while the quit confirmation overlay is open (Quit was requested
    /// with toolbox runs still in flight; confirming kills them and exits).
    ConfirmingQuit: bool
    /// "Allow secret access?" overlay. None = closed. Opened by startRun
    /// when a flow references secret params without a current grant.
    GrantPrompt: GrantPromptState option
    /// Cached `.takatora/params.toml` declarations per project (for the
    /// Settings shared-params section). Refreshed alongside ProjectSecrets.
    ProjectParams: Map<ProjectId, ProjectParam list>
    /// In-progress secret-param value texts in Settings, keyed by
    /// (project, param name). Cleared on save.
    ParamSecretDrafts: Map<ProjectId * string, string>
}

